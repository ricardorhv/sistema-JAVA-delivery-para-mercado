    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        alterarEstados(false);
        
        String nome = tfNome.getText();
        String CPF = ffCPF.getText();
        String email = tfEmail.getText();
        String telefone = ffTelefone.getText();
        String dataDeNascimentoText = ffDataDeNascimento.getText();
        
        int day;
        int month;
        int year;
        
        try {
            day = Integer.parseInt(dataDeNascimentoText.substring(0, 2));
            month = Integer.parseInt(dataDeNascimentoText.substring(3, 5)) - 1;
            year = Integer.parseInt(dataDeNascimentoText.substring(6, 10));
        } catch(Exception e ) {
            day = 0;
            month = 0;
            year = 0;
        }
        
        Calendar calendar = Calendar.getInstance();
        calendar.set(year, month, day);
        Date dataDeNascimento = calendar.getTime();
        
        this.usuarioLogado.setNome(nome);
        this.usuarioLogado.setCPF(CPF);
        this.usuarioLogado.setDataDeNascimento(dataDeNascimento);
        this.usuarioLogado.setEmail(email);
        this.usuarioLogado.setTelefone(telefone);
        
        
        if (usuarioLogado.validarDados()) {
            this.usuarioBD.alterarUsuario(this.usuarioLogado.getIdUsuario(), this.usuarioLogado);
            JOptionPane.showMessageDialog(this, "Dados alterados com sucesso!");
        }
    }//GEN-LAST:event_btnSalvarMouseClicked

